#include "libft.h"

long long	ft_atoi(const char *str)
{
	long long	ret;
	int	sign;
	int	i;

	i = 0;
	ret = 0;
	sign = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		ret = ((ret * 10) + str[i] - 48);
		i++;
	}
	ret = ret * sign;
	return (ret);
}
